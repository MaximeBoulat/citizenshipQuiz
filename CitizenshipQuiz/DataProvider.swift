class DataProvider: DataProviding {
	
	private var deck: [QuizItem] = []
	
	func resetDeck() {
		deck = dataSource
	}
	
	func getPair() -> QuizItem? {
		
		if !deck.isEmpty {
			let randomIndex = Int.random(in: 0..<deck.count)
			
			print("picked random index: \(randomIndex)")
			let item = deck.remove(at: randomIndex)
			
			print("count remaining: \(deck.count)")
			return item
		}
		else {
			print("empty deck")
			return nil
		}
	}
	
}
